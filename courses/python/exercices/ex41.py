# Exercício 41
# O que é um parâmetro?
# Parâmetro é uma variável criada dentro da função
# para receber informações quando ela for chamada.

# Procedimento que recebe um parâmetro
def mostrarJogador(nome):
    # Exibe o nome recebido
    print(f"Jogador: {nome}")

# Chamada do procedimento passando um argumento
mostrarJogador("Carlos")