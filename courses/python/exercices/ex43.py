# O que é uma função com retorno?
# É uma função que devolve um valor utilizando a palavra-chave return.

# Função que recebe uma pontuação e retorna o dobro dela
def dobrarPontos(pontos):
    return pontos * 2

# Chamada da função
resultado = dobrarPontos(50)

# Exibe o valor retornado
print(f"Pontuação dobrada: {resultado}")