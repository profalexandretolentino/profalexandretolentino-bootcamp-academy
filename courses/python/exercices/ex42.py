# Exercício 42
# O que é um argumento?
# Argumento é o valor enviado para um parâmetro quando
# a função ou procedimento é chamado.

# Procedimento que recebe um parâmetro
def mostrarJogador(nome):
    # Exibe o nome recebido
    print(f"Jogador: {nome}")

# Chamadas do procedimento com diferentes argumentos
mostrarJogador("Carlos")
mostrarJogador("Ana")
mostrarJogador("Pedro")
mostrarJogador("Mariana")