let a = 10;
let b = 3;

// Operações básicas
const soma = a + b;
const subtracao = a - b;
const multiplicacao = a * b;
const divisao = a / b;
const modulo = a % b;

// Operador unário (-)
const negativoA = -a;

// Incremento e decremento
let contador = 5;

contador++; // incremento
contador--; // decremento

console.log("Soma:", soma);
console.log("Subtração:", subtracao);
console.log("Multiplicação:", multiplicacao);
console.log("Divisão:", divisao);
console.log("Módulo:", modulo);
console.log("Negativo de a:", negativoA);
contador++; // incremento
console.log("Valor final do contador com incremento:", contador);
contador--; // decremento
console.log("Valor final do contador com decremento:", contador);